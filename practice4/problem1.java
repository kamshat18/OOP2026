
//1. What is the core difference between a class and an interface?
//A class is a blueprint for creating objects and can contain implemented methods and fields.
//An interface defines a contract of methods that a class must implement, without providing full implementation (except default methods).
//
//2. Can interfaces have fields? If yes, what are the implicit modifiers for them?
//Yes, interfaces can have fields.
//All fields in an interface are implicitly public, static, and final.
//
//3. Can a class implement multiple interfaces? Explain why this is useful.
//Yes, a class can implement multiple interfaces.
//This is useful because it allows a class to inherit behavior from multiple sources and supports flexible design without multiple inheritance of classes.